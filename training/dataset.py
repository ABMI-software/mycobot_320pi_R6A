#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""PyTorch Dataset for MyCobot synthetic pose estimation data.

Loads (image, joint_angles) pairs from the dataset generated by
``synthetic_data_collector``.

Expected directory layout::

    dataset_dir/
    ├── images/
    │   ├── 000000.png
    │   └── ...
    └── labels.csv
"""

import csv
import os
from typing import Callable, List, Optional, Tuple

import numpy as np
import torch
from PIL import Image
from torch.utils.data import Dataset
from torchvision import transforms


# ---------- Normalisation constants ----------
# Joint limits (radians) from URDF — used for optional normalisation to [-1, 1]
JOINT_LIMITS = np.array([
    [-2.96, 2.96],   # joint1
    [-2.79, 2.79],   # joint2
    [-2.79, 2.79],   # joint3
    [-2.79, 2.79],   # joint4
    [-2.96, 2.96],   # joint5
    [-3.05, 3.05],   # joint6
], dtype=np.float32)

JOINT_RANGE = JOINT_LIMITS[:, 1] - JOINT_LIMITS[:, 0]   # span per joint
JOINT_MID   = (JOINT_LIMITS[:, 1] + JOINT_LIMITS[:, 0]) / 2.0


def normalize_angles(angles: np.ndarray) -> np.ndarray:
    """Map raw radians → [-1, 1] using known joint limits."""
    return (angles - JOINT_MID) / (JOINT_RANGE / 2.0)


def denormalize_angles(normed: np.ndarray) -> np.ndarray:
    """Inverse of :func:`normalize_angles`."""
    return normed * (JOINT_RANGE / 2.0) + JOINT_MID


# ---------- Default image transforms ----------
def get_train_transforms(image_size: int = 224) -> transforms.Compose:
    """Augmented transforms for training."""
    return transforms.Compose([
        transforms.Resize((image_size, image_size)),
        transforms.ColorJitter(brightness=0.2, contrast=0.2, saturation=0.2, hue=0.05),
        transforms.RandomGrayscale(p=0.05),
        transforms.GaussianBlur(kernel_size=3, sigma=(0.1, 1.0)),
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485, 0.456, 0.406],
                             std=[0.229, 0.224, 0.225]),
    ])


def get_eval_transforms(image_size: int = 224) -> transforms.Compose:
    """Deterministic transforms for validation / inference."""
    return transforms.Compose([
        transforms.Resize((image_size, image_size)),
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485, 0.456, 0.406],
                             std=[0.229, 0.224, 0.225]),
    ])


# ---------- Dataset ----------
class MyCobotPoseDataset(Dataset):
    """Dataset of (image, joint_angles) pairs.

    Parameters
    ----------
    dataset_dir : str
        Root directory containing ``images/`` and ``labels.csv``.
    transform : callable, optional
        Image transform pipeline.  Defaults to eval transforms.
    normalize_targets : bool
        If *True* the joint angles are scaled to [-1, 1].
    """

    RAD_COLS = ['j1_rad', 'j2_rad', 'j3_rad', 'j4_rad', 'j5_rad', 'j6_rad']

    def __init__(
        self,
        dataset_dir: str,
        transform: Optional[Callable] = None,
        normalize_targets: bool = True,
    ):
        self.dataset_dir = dataset_dir
        self.transform = transform or get_eval_transforms()
        self.normalize_targets = normalize_targets

        # Load CSV
        csv_path = os.path.join(dataset_dir, 'labels.csv')
        self.samples: List[Tuple[str, np.ndarray]] = []
        with open(csv_path, 'r') as f:
            reader = csv.DictReader(f)
            for row in reader:
                img_rel = row['image_path']
                angles = np.array([float(row[c]) for c in self.RAD_COLS],
                                  dtype=np.float32)
                self.samples.append((img_rel, angles))

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, torch.Tensor]:
        img_rel, angles = self.samples[idx]
        img_path = os.path.join(self.dataset_dir, img_rel)

        image = Image.open(img_path).convert('RGB')
        image = self.transform(image)

        if self.normalize_targets:
            angles = normalize_angles(angles)

        target = torch.from_numpy(angles)
        return image, target
